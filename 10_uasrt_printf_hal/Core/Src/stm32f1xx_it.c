// 函数：外部中断回调函数
// 参数：GPIO引脚
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
  // 如果引脚是KEY1引脚
  if (GPIO_Pin == KEY1_Pin)
  {
    // 延时15ms
    HAL_Delay(15);
    // 如果KEY1引脚的电平为低
    if (HAL_GPIO_ReadPin(GPIOA, KEY1_Pin) != GPIO_PIN_RESET)
    {
      // 切换LED1引脚的电平
      HAL_GPIO_TogglePin(GPIOB, LED1_Pin);
    }
  }
}

/**
 * 简    介：UART接收完成回调函数（空闲线检测触发）
 * 参    数：
 *   huart - UART句柄指针
 *   Size  - 接收到的数据长度
 * 返 回 值：无
 * 功能描述：
 * 1. 当检测到空闲线事件时自动调用
 * 2. 更新接收完成标志和实际接收长度
 */
void HAL_UARTEx_RxEventCallback(UART_HandleTypeDef *huart, uint16_t Size)
{
  ReceptionIsComplete = 1; // 设置接收完成标志
  len = Size;              // 保存实际接收长度
}
